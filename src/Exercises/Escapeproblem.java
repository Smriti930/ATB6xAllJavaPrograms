package Practise;

public class Escapeproblem {

    public static void main(String[] args) {
        System.out.println("This is tab \t"+"Tab space");
        System.out.println("This is backspace\b"+" "+"backspace character");
        System.out.println("This is new line\n"+"newline");
        System.out.println("This is Carriage Return\r"+"Carriage Return");
        System.out.println("This is formfeed\f"+" "+"formfeed");
        System.out.println("This is single quote character\'"+" "+"single quote character");
        System.out.println("This is double quote character\""+" "+"double quote character");
        System.out.println("This is backslash character\"" + "backslash character");
    }
}
