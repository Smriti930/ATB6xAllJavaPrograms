package Practise;
//ASCII value of characters
public class Quest16 {
    public static void main(String[] args) {
        char c;
        int as;
        for (c = 'a'; c <='z' ; c++) {
            as=(int)c;
            System.out.println("ASCII value of "+c+" is "+as);

        }
        for (c = 'A'; c <='Z' ; c++) {
            as=(int)c;
            System.out.println("ASCII value of "+c+" is "+as);

        }


    }
}
